# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –∏–¥–µ–∏
def generate_startup_idea(preferences):
    preferences = preferences.lower().strip()
    
    # –í—ã–±–∏—Ä–∞–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é –Ω–∞ –æ—Å–Ω–æ–≤–µ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏–π
    if "—Ç–µ—Ö–Ω–æ–ª–æ–≥" in preferences:
        category = "—Ç–µ—Ö–Ω–æ–ª–æ–≥–∏–∏"
    elif "–æ–±—Ä–∞–∑–æ–≤–∞–Ω" in preferences or "–æ–±—É—á–µ–Ω" in preferences:
        category = "–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ"
    elif "–∑–¥–æ—Ä–æ–≤" in preferences or "–º–µ–¥–∏—Ü" in preferences:
        category = "–∑–¥–æ—Ä–æ–≤—å–µ"
    elif "–µ–¥–∞" in preferences or "–ø–∏—Ç–∞–Ω" in preferences or "–∫—É–ª–∏–Ω–∞—Ä" in preferences:
        category = "–µ–¥–∞"
    elif "—Å–ø–æ—Ä—Ç" in preferences or "—Ñ–∏—Ç–Ω–µ—Å" in preferences:
        category = "—Å–ø–æ—Ä—Ç"
    else:
        category = "default"
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é –∏–¥–µ—é –∏–∑ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    idea = random.choice(startup_ideas.get(category, startup_ideas["default"]))
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∏–¥–µ—é –≤ –∏—Å—Ç–æ—Ä–∏—é
    idea_with_date = {
        "idea": idea,
        "date": datetime.now().strftime("%d.%m.%Y %H:%M"),
        "preferences": preferences
    }
    st.session_state.ideas_history.append(idea_with_date)
    
    return idea

